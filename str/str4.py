# replace : 문자열 교체 함수
print("banana".replace("a", "o"))

print("010-1234-5678".replace("-",":"))

print("010-1234-5678".replace("-",""))

# repr : 따음표 표시하는 함수
print("hello")
print(repr("hello"))

# 공백 제거 함수들
print(repr(" hello world ".strip()))
print(repr(" hello world ".lstrip()))
print(repr(" hello world ".rstrip()))

print("hello world".title())